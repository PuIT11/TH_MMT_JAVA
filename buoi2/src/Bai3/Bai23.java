    private void btListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btListActionPerformed
        // TODO add your handling code here:
        txtXuat1.setText(null);
        txtXuat1.setText("Số nguyên từ số " +txtBatDau.getText()+" đến số " +txtKetThuc.getText()+"\n");
        int s1= Integer.parseInt(txtBatDau.getText());
        int s2= Integer.parseInt(txtKetThuc.getText());
        int dem=1;
        for(; s1<=s2; s1++){
            txtXuat1.append(s1+"\t");
            if(dem%5==0)
                txtXuat1.append("\n"); 
            dem++;           
        }
    }//GEN-LAST:event_btListActionPerformed

    private boolean KTSNT(int a){
        if(a<2)
            return false;
        for(int i=2; i<=(int)Math.sqrt(a); i++)
        {
            if(a%i==0)
            return false;
        }
        return true;
    }
    
