    private void btDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDocActionPerformed
        // TODO add your handling code here:
        try{
            FileReader fr=new FileReader(new File(txtNameTapTin.getText()));
            StringBuffer sb=new StringBuffer();
            char ca[] = new char[5];
            while(fr.ready()){
                int len = fr.read(ca);
                sb.append(ca,0,len);
            }
            fr.close();
            sb.delete(0, sb.indexOf("\n") + 1);
            txtXuat2.setText(sb+"");
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btDocActionPerformed

    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        // TODO add your handling code here:
        int s1= Integer.parseInt(txtBatDau.getText());
        int s2= Integer.parseInt(txtKetThuc.getText());
        try{
            FileWriter fw= new FileWriter(new File(txtNameTapTin.getText()));
            fw.write("Số nguyên tố từ số " +txtBatDau.getText()+" đến số " +txtKetThuc.getText()+"\n");
            int d=1;
            
            for(;s1<=s2;s1++){
               if(KTSNT(s1)){
                   fw.write(s1+"\t");
                   if(d%5==0)
                       fw.write("\n");
                   d++;
               } 
            }
            fw.close();
        }catch(Exception e){
           e.printStackTrace();
        }
    }//GEN-LAST:event_btSaveActionPerformed

